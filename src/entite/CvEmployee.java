package entite;

public class CvEmployee {

    private int numEmployee;
    private String diplome;
    private int nbAnneeExperience;
    private String formation;
    private String stage;
    public int getNumEmployee() {
        return numEmployee;
    }
    public void setNumEmployee(int numEmployee) {
        this.numEmployee = numEmployee;
    }
    public String getDiplome() {
        return diplome;
    }
    public void setDiplome(String diplome) {
        this.diplome = diplome;
    }
    public int getNbAnneeExperience() {
        return nbAnneeExperience;
    }
    public void setNbAnneeExperience(int nbAnneeExperience) {
        this.nbAnneeExperience = nbAnneeExperience;
    }
    public String getFormation() {
        return formation;
    }
    public void setFormation(String formation) {
        this.formation = formation;
    }
    public String getStage() {
        return stage;
    }
    public void setStage(String stage) {
        this.stage = stage;
    }

}
